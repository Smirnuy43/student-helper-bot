def build_prompt(text: str, language: str = "Python") -> str:
    """
    Формирует промпт на основе текста задания.
    """
    return (
        f"Ниже приведено задание, которое прислал пользователь.\n\n"
        f"Задача:\n{text.strip()}\n\n"
        f"Требуется: Напиши решение на языке {language}.\n"
        f"Выводи только код, без лишних комментариев и пояснений."
    )
